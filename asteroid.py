import pygame
import random
from circleshape import CircleShape
from constants import *
from logger import log_event

class Asteroid(CircleShape):
    def __init__(self, x, y, radius):
        super().__init__(x, y, radius)

    def draw(self, screen):
        pygame.draw.circle(screen, "white", self.position, self.radius, LINE_WIDTH)
    
    def update(self, dt):
        self.position += (self.velocity * dt)
        pass

    def split(self):
        self.kill()

        if self.radius <= ASTEROID_MIN_RADIUS:
            return
        else:
            log_event("asteroid_split")
            random_angle = random.uniform(20, 50)
            new_velocity_one = self.velocity.rotate(random_angle)
            new_velocity_two = self.velocity.rotate(-random_angle)

            new_radius = self.radius - ASTEROID_MIN_RADIUS

            asteroid_object_one = Asteroid(self.position[0], self.position[1], new_radius)
            asteroid_object_two = Asteroid(self.position[0], self.position[1], new_radius)

            asteroid_object_one.velocity = new_velocity_one * 1.2
            asteroid_object_two.velocity = new_velocity_two * 1.2
